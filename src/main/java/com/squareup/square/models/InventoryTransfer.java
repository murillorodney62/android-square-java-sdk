package com.squareup.square.models;

import java.util.Objects;
import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonGetter;

public class InventoryTransfer {

    @JsonCreator
    public InventoryTransfer(
            @JsonProperty("id") String id,
            @JsonProperty("reference_id") String referenceId,
            @JsonProperty("state") String state,
            @JsonProperty("from_location_id") String fromLocationId,
            @JsonProperty("to_location_id") String toLocationId,
            @JsonProperty("catalog_object_id") String catalogObjectId,
            @JsonProperty("catalog_object_type") String catalogObjectType,
            @JsonProperty("quantity") String quantity,
            @JsonProperty("occurred_at") String occurredAt,
            @JsonProperty("created_at") String createdAt,
            @JsonProperty("source") SourceApplication source,
            @JsonProperty("employee_id") String employeeId) {
        this.id = id;
        this.referenceId = referenceId;
        this.state = state;
        this.fromLocationId = fromLocationId;
        this.toLocationId = toLocationId;
        this.catalogObjectId = catalogObjectId;
        this.catalogObjectType = catalogObjectType;
        this.quantity = quantity;
        this.occurredAt = occurredAt;
        this.createdAt = createdAt;
        this.source = source;
        this.employeeId = employeeId;
    }

    private final String id;
    private final String referenceId;
    private final String state;
    private final String fromLocationId;
    private final String toLocationId;
    private final String catalogObjectId;
    private final String catalogObjectType;
    private final String quantity;
    private final String occurredAt;
    private final String createdAt;
    private final SourceApplication source;
    private final String employeeId;

    @Override
    public int hashCode() {
        return Objects.hash(id, referenceId, state, fromLocationId, toLocationId, catalogObjectId, catalogObjectType, quantity, occurredAt, createdAt, source, employeeId);
    }

    @Override
    public boolean equals(Object o) {
        if (o == this)
            return true;
        if (!(o instanceof InventoryTransfer)) {
            return false;
        }
        InventoryTransfer inventoryTransfer = (InventoryTransfer) o;
        return Objects.equals(id, inventoryTransfer.id) &&
            Objects.equals(referenceId, inventoryTransfer.referenceId) &&
            Objects.equals(state, inventoryTransfer.state) &&
            Objects.equals(fromLocationId, inventoryTransfer.fromLocationId) &&
            Objects.equals(toLocationId, inventoryTransfer.toLocationId) &&
            Objects.equals(catalogObjectId, inventoryTransfer.catalogObjectId) &&
            Objects.equals(catalogObjectType, inventoryTransfer.catalogObjectType) &&
            Objects.equals(quantity, inventoryTransfer.quantity) &&
            Objects.equals(occurredAt, inventoryTransfer.occurredAt) &&
            Objects.equals(createdAt, inventoryTransfer.createdAt) &&
            Objects.equals(source, inventoryTransfer.source) &&
            Objects.equals(employeeId, inventoryTransfer.employeeId);
    }

    /**
     * Getter for Id.
     * A unique ID generated by Square for the
     * `InventoryTransfer`.
     */
    @JsonGetter("id")
    public String getId() { 
        return this.id;
    }

    /**
     * Getter for ReferenceId.
     * An optional ID provided by the application to tie the
     * `InventoryTransfer` to an external system.
     */
    @JsonGetter("reference_id")
    public String getReferenceId() { 
        return this.referenceId;
    }

    /**
     * Getter for State.
     * Indicates the state of a tracked item quantity in the lifecycle of goods.
     */
    @JsonGetter("state")
    public String getState() { 
        return this.state;
    }

    /**
     * Getter for FromLocationId.
     * The Square ID of the [Location](#type-location) where the related
     * quantity of items were tracked before the transfer.
     */
    @JsonGetter("from_location_id")
    public String getFromLocationId() { 
        return this.fromLocationId;
    }

    /**
     * Getter for ToLocationId.
     * The Square ID of the [Location](#type-location) where the related
     * quantity of items were tracked after the transfer.
     */
    @JsonGetter("to_location_id")
    public String getToLocationId() { 
        return this.toLocationId;
    }

    /**
     * Getter for CatalogObjectId.
     * The Square generated ID of the
     * `CatalogObject` being tracked.
     */
    @JsonGetter("catalog_object_id")
    public String getCatalogObjectId() { 
        return this.catalogObjectId;
    }

    /**
     * Getter for CatalogObjectType.
     * The `CatalogObjectType` of the
     * `CatalogObject` being tracked.Tracking is only
     * supported for the `ITEM_VARIATION` type.
     */
    @JsonGetter("catalog_object_type")
    public String getCatalogObjectType() { 
        return this.catalogObjectType;
    }

    /**
     * Getter for Quantity.
     * The number of items affected by the transfer as a decimal string.
     * Can support up to 5 digits after the decimal point.
     * _Important_: The Point of Sale app and Dashboard do not currently support
     * decimal quantities. If a Point of Sale app or Dashboard attempts to read a
     * decimal quantity on inventory counts or adjustments, the quantity will be rounded
     * down to the nearest integer. For example, `2.5` will become `2`, and `-2.5`
     * will become `-3`.
     * Read [Decimal Quantities (BETA)](https://developer.squareup.com/docs/docs/inventory-api/what-it-does#decimal-quantities-beta) for more information.
     */
    @JsonGetter("quantity")
    public String getQuantity() { 
        return this.quantity;
    }

    /**
     * Getter for OccurredAt.
     * A client-generated timestamp in RFC 3339 format that indicates when
     * the transfer took place. For write actions, the `occurred_at` timestamp
     * cannot be older than 24 hours or in the future relative to the time of the
     * request.
     */
    @JsonGetter("occurred_at")
    public String getOccurredAt() { 
        return this.occurredAt;
    }

    /**
     * Getter for CreatedAt.
     * A read-only timestamp in RFC 3339 format that indicates when Square
     * received the transfer request.
     */
    @JsonGetter("created_at")
    public String getCreatedAt() { 
        return this.createdAt;
    }

    /**
     * Getter for Source.
     * Provides information about the application used to generate an inventory
     * change.
     */
    @JsonGetter("source")
    public SourceApplication getSource() { 
        return this.source;
    }

    /**
     * Getter for EmployeeId.
     * The Square ID of the [Employee](#type-employee) responsible for the
     * inventory transfer.
     */
    @JsonGetter("employee_id")
    public String getEmployeeId() { 
        return this.employeeId;
    }

 
    public Builder toBuilder() {
        Builder builder = new Builder()
            .id(getId())
            .referenceId(getReferenceId())
            .state(getState())
            .fromLocationId(getFromLocationId())
            .toLocationId(getToLocationId())
            .catalogObjectId(getCatalogObjectId())
            .catalogObjectType(getCatalogObjectType())
            .quantity(getQuantity())
            .occurredAt(getOccurredAt())
            .createdAt(getCreatedAt())
            .source(getSource())
            .employeeId(getEmployeeId());
            return builder;
    }

    public static class Builder {
        private String id;
        private String referenceId;
        private String state;
        private String fromLocationId;
        private String toLocationId;
        private String catalogObjectId;
        private String catalogObjectType;
        private String quantity;
        private String occurredAt;
        private String createdAt;
        private SourceApplication source;
        private String employeeId;

        public Builder() { }

        public Builder id(String value) {
            id = value;
            return this;
        }
        public Builder referenceId(String value) {
            referenceId = value;
            return this;
        }
        public Builder state(String value) {
            state = value;
            return this;
        }
        public Builder fromLocationId(String value) {
            fromLocationId = value;
            return this;
        }
        public Builder toLocationId(String value) {
            toLocationId = value;
            return this;
        }
        public Builder catalogObjectId(String value) {
            catalogObjectId = value;
            return this;
        }
        public Builder catalogObjectType(String value) {
            catalogObjectType = value;
            return this;
        }
        public Builder quantity(String value) {
            quantity = value;
            return this;
        }
        public Builder occurredAt(String value) {
            occurredAt = value;
            return this;
        }
        public Builder createdAt(String value) {
            createdAt = value;
            return this;
        }
        public Builder source(SourceApplication value) {
            source = value;
            return this;
        }
        public Builder employeeId(String value) {
            employeeId = value;
            return this;
        }

        public InventoryTransfer build() {
            return new InventoryTransfer(id,
                referenceId,
                state,
                fromLocationId,
                toLocationId,
                catalogObjectId,
                catalogObjectType,
                quantity,
                occurredAt,
                createdAt,
                source,
                employeeId);
        }
    }
}
